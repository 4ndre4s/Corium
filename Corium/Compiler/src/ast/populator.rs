use crate::ast::*;
use crate::parser::*;
use std::str::FromStr;

pub trait AstPopulator<'a>: AstComponent {
    fn populate(rule: RulePair<'a>) -> Self;

    fn map(mut rule: RulePairs<'a>) -> Self {
        let rule = rule
            .next()
            .unwrap_or_else(|| panic!("Expected another inner rule, but rule was empty!"));
        debug_assert_eq!(Self::CORRESPONDING_RULE, rule.as_rule());
        Self::populate(rule)
    }
}

impl<'a> AstPopulator<'a> for CompilationUnit<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let mut rule = rule.into_inner();

        let module = Module::map(rule.clone());
        let mut statements = Vec::new();

        rule.next();

        // GlobalStatement*
        'fetch: while let Some(inner) = rule.peek() {
            if inner.as_rule() == Rule::EOI {
                break 'fetch;
            } else if inner.as_rule() == Rule::GlobalStatement {
                statements.push(GlobalStatement::map(rule.clone()));
                rule.next();
            }
        }

        Self { module, statements }
    }
}

impl<'a> AstPopulator<'a> for GlobalStatement<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let rule = rule.into_inner();
        let nested = rule.clone().next().unwrap();
        match nested.as_rule() {
            Rule::Function => Self::Function(Function::map(rule)),
            Rule::NativeFunction => Self::NativeFunction(NativeFunction::map(rule)),
            _ => unreachable!(),
        }
    }
}

impl<'a> AstPopulator<'a> for Function<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let mut rule = rule.into_inner();
        let signature = {
            let inner = rule.clone().next().unwrap();
            debug_assert_eq!(inner.as_rule(), Rule::FunctionSignature);
            FunctionSignature::map(rule.clone())
        };
        rule.next(); // advance
        let block = {
            let inner = rule.clone().next().unwrap();
            debug_assert_eq!(inner.as_rule(), Rule::Block);
            Block::map(rule.clone())
        };
        Self { signature, block }
    }
}

impl<'a> AstPopulator<'a> for NativeFunction<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let rule = rule.into_inner();
        let inner = rule.clone().next().unwrap();
        debug_assert_eq!(inner.as_rule(), Rule::FunctionSignature);
        Self {
            signature: FunctionSignature::map(rule.clone()),
        }
    }
}

impl<'a> AstPopulator<'a> for FunctionSignature<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let mut rule = rule.into_inner();

        // ehm yes this is not that beautiful - TODO make this nicer
        let name = {
            let inner = rule.clone().next().unwrap();
            debug_assert_eq!(inner.as_rule(), Rule::Identifier);
            Identifier::map(rule.clone())
        };

        rule.next(); // advance

        let (parameters, return_type) = if let Some(inner) = rule.clone().next() {
            match inner.as_rule() {
                Rule::ParameterList => {
                    let parameters = Some(ParameterList::map(rule.clone()));
                    let mut return_type = None;
                    if rule.next().is_some() {
                        if let Some(inner) = rule.clone().next() {
                            debug_assert_eq!(inner.as_rule(), Rule::QualifiedName);
                            return_type = Some(QualifiedName::map(rule.clone()));
                        }
                    }
                    (parameters, return_type)
                }
                Rule::QualifiedName => (None, Some(QualifiedName::map(rule.clone()))),
                _ => unreachable!(),
            }
        } else {
            (None, None)
        };

        Self {
            name,
            parameters,
            return_type,
        }
    }
}

impl<'a> AstPopulator<'a> for Block<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let mut rule = rule.into_inner();
        let mut result = Vec::new();
        // FunctionStatement*
        while rule.peek().is_some() {
            result.push(FunctionStatement::map(rule.clone()));
            rule.next();
        }
        Self(result)
    }
}

impl<'a> AstPopulator<'a> for FunctionStatement<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let rule = rule.into_inner();
        let nested = rule.clone().next().unwrap();
        match nested.as_rule() {
            Rule::LocalVariable => Self::LocalVariable(LocalVariable::map(rule)),
            Rule::ReturnStatement => Self::ReturnStatement(ReturnStatement::map(rule)),
            _ => unreachable!(),
        }
    }
}

impl<'a> AstPopulator<'a> for Module<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        Self(QualifiedName::map(rule.into_inner()))
    }
}

impl<'a> AstPopulator<'a> for LocalVariable<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let mut rule = rule.into_inner();

        // ehm yes this is not that beautiful - TODO make this nicer

        let name = {
            let inner = rule.clone().next().unwrap();
            debug_assert_eq!(inner.as_rule(), Rule::Identifier);
            Identifier::map(rule.clone())
        };
        // advance
        rule.next();

        let inner = rule.clone().next().unwrap();
        let (type_hint, value) = match inner.as_rule() {
            Rule::QualifiedName => {
                let type_hint = Some(QualifiedName::map(rule.clone()));
                rule.next(); // advance
                debug_assert_eq!(rule.clone().next().unwrap().as_rule(), Rule::Expression);
                (type_hint, Expression::map(rule.clone()))
            }
            Rule::Expression => (None, Expression::map(rule)),
            _ => unreachable!(),
        };

        Self {
            name,
            type_hint,
            value,
        }
    }
}

impl<'a> AstPopulator<'a> for ReturnStatement<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        if rule.clone().into_inner().next().is_some() {
            Self(Some(Expression::map(rule.into_inner())))
        } else {
            Self(None)
        }
    }
}

impl<'a> AstPopulator<'a> for ParameterList<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let text = rule.as_str();
        if text.is_empty() {
            return Self(Vec::new());
        }
        let mut rule = rule.into_inner();
        let mut result = Vec::new();

        // params*
        while let Some(inner) = rule.peek() {
            debug_assert_eq!(inner.as_rule(), Rule::Parameter);
            result.push(Parameter::map(rule.clone()));
            rule.next();
        }
        Self(result)
    }
}

impl<'a> AstPopulator<'a> for Parameter<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let mut rule = rule.into_inner();

        // ehm yes this is not that beautiful - TODO make this nicer

        let name = {
            let inner = rule.clone().next().unwrap();
            debug_assert_eq!(inner.as_rule(), Rule::Identifier);
            Identifier::map(rule.clone())
        };

        // advance
        rule.next();

        let type_hint = {
            let inner = rule.clone().next().unwrap();
            debug_assert_eq!(inner.as_rule(), Rule::QualifiedName);
            QualifiedName::map(rule.clone())
        };

        let value = if rule.next().is_some() {
            if let Some(inner) = rule.clone().next() {
                debug_assert_eq!(inner.as_rule(), Rule::Expression);
                Some(Expression::map(rule.clone()))
            } else {
                None
            }
        } else {
            None
        };

        Self {
            name,
            type_hint,
            value,
        }
    }
}

impl<'a> AstPopulator<'a> for Expression<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let rule = rule.into_inner();
        let kind = rule.clone().next().unwrap().as_rule();
        match kind {
            Rule::Literal => Self::Literal(Literal::map(rule)),
            _ => unreachable!(),
        }
    }
}

impl<'a> AstPopulator<'a> for Literal<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        let rule = rule.into_inner().next().unwrap();
        let kind = rule.as_rule();
        let text = rule.as_str();
        match kind {
            Rule::FloatLiteral => Self::Float(Float::from_str(text).unwrap()),
            Rule::IntLiteral => Self::Int(Int::from_str(text).unwrap()),
            Rule::BoolLiteral => Self::Bool(Bool::from_str(text).unwrap()),
            Rule::CharLiteral => {
                let text = &text[1..text.len() - 1]; // skip ''
                let mut text = text.to_string();
                text = text.replace(r#"\""#, "\"");
                text = text.replace(r#"\\"#, "\\");
                text = text.replace(r#"\n"#, "\n");
                text = text.replace(r#"\r"#, "\r");
                text = text.replace(r#"\t"#, "\t");
                Self::Char(Char::from_str(&text).unwrap())
            }
            Rule::StringLiteral => {
                let text = &text[1..text.len() - 1]; // skip ""
                Self::String(text)
            }
            _ => unreachable!(),
        }
    }
}

impl<'a> AstPopulator<'a> for QualifiedName<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        Self {
            full: rule.as_str(),
            split: rule.as_str().split('.').collect(),
        }
    }
}

impl<'a> AstPopulator<'a> for Identifier<'a> {
    fn populate(rule: RulePair<'a>) -> Self {
        Self(rule.as_str())
    }
}
