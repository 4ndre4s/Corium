/* Converts string to lower */
static void txt_2_lower(register char *_in) {

    for(; *_in; ++_in) *_in = tolower(*_in);
}

/* Removes whitespaces */
static void remove_spaces(register char *_in) {

    register char *b = _in;
    do { while(*b == L' ') ++b; } while((*_in++ = *b++));
}

/* Remove tabs and newlines */
static void remove_tabs_newlines(register char *_in) {

    for(; *_in; ++_in) if(*_in == L'\n' || *_in == L'\t') *_in = '\0';
}

static int prepare_line(char *const _buf) {

    txt_2_lower(_buf);
    remove_spaces(_buf);
    remove_tabs_newlines(_buf);

    return *_buf != '\0' && *_buf != TOKEN_COMMENT;
}

static int safe_strcpy(char *const _dst, const char *const _src, const unsigned long long _dst_size) {

    if(strlen(_src) > _dst_size) {

        return FALSE;
    }

    return strncpy(_dst, _src, _dst_size) ? TRUE : FALSE;
}

static char *replace(const char *const _search, const char *const _replace, char *const _str) {

    const char *const  ss = strstr(_str, _search);
    if(!ss) {
        return _str;
    }

    char * tmp;
    g_alloc((void**)&tmp, strlen(_str) * sizeof(char), MEM_NONE);
    if(EXCEPT()) {
        return NULLPTR;
    }

    strcpy(tmp, _str);
    unsigned long long len = ss - _str;
    _str[len] = '\0';
    strcat(_str, _replace);
    len += strlen(_search);
    strcat(_str, (char*)tmp+len);

    free(tmp);

    CLEAR_EXSTATUS();
    return _str;
}

static enum sec_t determine_section(register char *const _buf) {

    register enum sec_t i;
    for(i = 0; i < SEC_NUM; ++i)  {

        if(!strncmp(_buf, g_sec_mnics[i], strlen(g_sec_mnics[i]))) {

            return i;
        }
    }

    return SEC_NUM;
}

static enum pp_instr_t determine_pp_instr(register char **const _buf) {

    register enum pp_instr_t i;
    for(i = 0; i < PPI_NUM; ++i) {

        const char *const mnemonic = g_pp_mnics[i];
        const unsigned long long len = strlen(mnemonic);

        if(!strncmp(*_buf, mnemonic, len)) {

            *_buf += len;
            return i;
        }
    }

    return PPI_NUM;
}

static enum op_t determine_instruction(register char **const _buf, const struct op_meta_t * *const _out) {

    register const struct op_meta_t *opm;
    register enum op_t found = OP_NUM;
    register enum op_t i;

    for(i = 0; i < OP_NUM; ++i) {

        register const struct op_meta_t *tmp = tmp = g_op_meta + i;

        if(!strncmp(*_buf, tmp->mnemonic, strlen(tmp->mnemonic))) {

            opm = tmp;
            found = i;
        }
    }

    if(found == OP_NUM) {

        *_out = NULLPTR;
        return found;
    }

    *_out = opm;
    *_buf += strlen(opm->mnemonic);

    return found;
}

static int check_file_extension(const char * const _path, const char * const _ext) {

    auto const unsigned long long a = strlen(_path), b = strlen(_ext);
    return !(b > a && strncmp(_path + a - b, _ext, b));
}

/* Maps to the bases of the corresponding number systems */
enum int_flags {

    ITF_NONE    = 0,
    ITF_HEX     = 16,
    ITF_BIN     = 2,
    ITF_DEC     = 10,
    ITF_OCT     = 8
};

static enum oppt_t determine_param_type(char ** const _buf, enum int_flags *const _fl) {

    switch(**_buf) {

        case TOKEN_NUMBER: {

            ++*_buf;
            switch(**_buf) {

                case TOKEN_SIGIL_INTHEX: {

                    *_fl = ITF_HEX;
                    ++*_buf;
                    if(**_buf != TOKEN_NUM_DELIM) {

                        *_fl = ITF_NONE;
                        return OPPT_NONE;
                    }
                    ++*_buf;
                    return OPPT_IPARAM;
                }

                case TOKEN_SIGIL_INTBIN: {

                    *_fl = ITF_BIN;
                    ++*_buf;
                    if(**_buf != TOKEN_NUM_DELIM) {

                        *_fl = ITF_NONE;
                        return OPPT_NONE;
                    }
                    ++*_buf;
                    return OPPT_IPARAM;
                }

                case TOKEN_SIGIL_INTOCT: {

                    *_fl = ITF_OCT;
                    ++*_buf;
                    if(**_buf != TOKEN_NUM_DELIM) {

                        *_fl = ITF_NONE;
                        return OPPT_NONE;
                    }
                    ++*_buf;
                    return OPPT_IPARAM;
                }

                default:
                case TOKEN_SIGIL_INTDEC: {

                    *_fl = ITF_DEC;
                    ++*_buf;
                    if(**_buf != TOKEN_NUM_DELIM) {

                        *_fl = ITF_NONE;
                        return OPPT_NONE;
                    }
                    ++*_buf;
                    return OPPT_IPARAM;
                }

                case TOKEN_SIGIL_FLOAT: {

                    *_fl = ITF_NONE;
                    ++*_buf;
                    if(**_buf != TOKEN_NUM_DELIM) {

                        *_fl = ITF_NONE;
                        return OPPT_NONE;
                    }
                    ++*_buf;
                    return OPPT_FPARAM;
                }

                case TOKEN_SIGIL_INTRIN: {

                    *_fl = ITF_HEX;
                    ++*_buf;
                    if(**_buf != TOKEN_NUM_DELIM) {

                        *_fl = ITF_NONE;
                        return OPPT_NONE;
                    }
                    ++*_buf;
                    return OPPT_INTID;
                }
            }
        }

        case TOKEN_PIN: {
            ++*_buf;
            *_fl = ITF_HEX;
            return OPPT_PPID;
        }

        default: {

            *_fl = ITF_NONE;
           return OPPT_NONE;
        }
    }
}

static int str_2_int(const char *const _buf, int *const _out, const enum int_flags _fl) {

    if(*_buf == '\0' || isspace(*_buf)) {
        return FALSE;
    }
    auto char *stub;
    errno = FALSE;
    auto const long val = strtol(_buf, &stub, _fl);
    /* Check for conversion failture or invalid number! */
    if(errno || val > INT_MAX || val < INT_MIN || *stub != '\0') {
        return FALSE;
    }
    *_out = (int)val;
    return TRUE;
}

static int str_2_float(const char *const _buf, float *const _out) {

    if(*_buf == '\0' || isspace(*_buf)) {
        return FALSE;
    }
    auto char *stub;
    errno = FALSE;
    auto const float val = strtof(_buf, &stub);
    /* Check for conversion failture or invalid number! */
    if(errno || val > FLT_MAX || val < FLT_MIN || *stub != '\0') {
        return FALSE;
    }
    *_out = val;
    return TRUE;
}

enum id_type {

    IDT_PIN,
    IDT_MACRO
};

struct id {

    char name[MAX_ID];
    char value[MAX_ID_VAL];
    enum id_type type;
    unsigned long long address;
};

/* Type identification table */
struct id_table {

    struct id *dat;
    unsigned long long num;
    unsigned long long cap;
};

static void id_table(struct id_table ** const _out, unsigned long long _n) {

    auto struct id_table * t;

    g_alloc((void**)&t, sizeof(struct id_table), MEM_NONE);
    if(EXCEPT()) {

        return;
    }

    if(!_n) {

        _n = DEF_LEN;
    }

    g_alloc((void**)&t->dat, sizeof(struct id) * _n, MEM_NONE);
    if(EXCEPT()) {

        return;
    }

    t->cap = _n;
    t->num = 0;

    *_out = t;

    CLEAR_EXSTATUS();
}

static void id_table_destroy(struct id_table ** const _inout) {

    g_dealloc((void**)&(*_inout)->dat);
    g_dealloc((void**)_inout);
}

static void id_table_push(struct id_table *const _t, struct id **const _o) {

    if(_t->num + 1 >= _t->cap) {   /* Not enough memory, reallocation required */
        g_realloc((void**)&_t->dat, sizeof(struct id) * (_t->cap << 1), MEM_NONE);
        if(EXCEPT()) {
            return;
        }
        _t->cap <<= 1;
    }
    ++_t->num;
    *_o = _t->dat + _t->num - 1;
    CLEAR_EXSTATUS();
}

static void id_table_pop(struct id_table *const _t) {

    _t->num < 0 ? 0 : --_t->num;
}

static unsigned long long id_table_find_duplicates(const struct id_table *const _t, const struct id *const _o) {

    register struct id *v;
    register unsigned long long num = 0;
    register unsigned long long i;
    for(i = 0; i < _t->num; ++i) {

        v = _t->dat + i;
        if(!strncmp(_o->name, v->name, strlen(_o->name))) {

            ++num;
        }
    }
    return num;
}

unsigned long long parse_from_file(struct prog_t **const _prog, const char *const _file, const enum interpreter_flags_t _flags) {

#ifdef DEBUG_VERBOSE
    auto const clock_t tik = clock();
#endif

    /* Check for invalid params */
    if(!_prog || !_file || *_file == '\0') {

        printf("Invalid parameters!\n");
        THROW(EX_INVALID_ARGUMENT);
        return 0;
    }

    /* Check for invalid file extension */
    if(!check_file_extension(_file, BYTECODE_TXT_EXT)) {

        printf("Invalid file extension: %s, must be: %s!\n", _file, BYTECODE_TXT_EXT);
        THROW(EX_FILE_STREAM);
        return 0;
    }

    auto file_t *filestream;
    file_open(&filestream, _file, FM_LOD, FF_TXT);
    if(EXCEPT()) {

        printf("Failed to open file: %s!\n", _file);
        return 0;
    }

    prog(_prog, DEF_LEN);
    if(EXCEPT()) {

        printf("Bytecode allocation failture!\n");
        return 0;
    }

    auto struct id_table *idtable;

    id_table(&idtable, DEF_LEN);
    if(EXCEPT()) {

        printf("Failed to allocate id table!\n");
        return 0;
    }

    auto struct prog_t *const pr = *_prog;
    auto enum sec_t csec = SEC_EXEC;
    auto unsigned long long lineidx;
    auto char linebuf[MAX_LINE];
    auto char *line;

    for(lineidx = 1; fgets(linebuf, sizeof(linebuf), filestream); ++lineidx) {

        if(!prepare_line(linebuf))
            continue;

        line = linebuf;

        /* Search macro in id table and replace all macros */
        register struct id *i, *end;

        for(i = idtable->dat, end = idtable->dat + idtable->num; i < end; ++i) {

            if(i->type == IDT_MACRO && strstr(line, i->name)) {

                replace(i->name, i->value, line);
            }
        }

        switch(*line) {

            case TOKEN_SECTION: {

                ++line;
                auto const enum sec_t sec = determine_section(line);

                if(sec == SEC_NUM) {

                    printf("[%s, %llu] Invalid section: %s!\n", _file, lineidx, line);
                    THROW(EX_BYTECODE);
                    return 0;
                }

                csec = sec;

            } break;

            case TOKEN_PREPROCESSOR: {

                ++line;

                /* Validate macro name */
                if(!*line || isdigit(*line)) {
                    printf("[%s, %llu] Invalid macro name: '%s'! Macro names cannot start with a digit!\n", _file, lineidx, line);
                    THROW(EX_BYTECODE);
                    return 0;
                }

                auto const enum pp_instr_t ppi = determine_pp_instr(&line);

                switch (ppi) {

                    case PPI_DEFINE: {   /* Add new macro to id table */

                        const char * const equ = strchr(line, TOKEN_EQUALS);
                        if(!equ) {

                            printf("[%s, %llu] Missing '%c' in macro defition!\n", _file, lineidx, TOKEN_EQUALS);
                            THROW(EX_BYTECODE);
                            return 0;
                        }

                        auto struct id *identry;
                        id_table_push(idtable, &identry);
                        if(EXCEPT()) {

                            printf("Failed to push id table!\n");
                            return 0;
                        }

                        identry->type = IDT_MACRO;
                        identry->address = 0;   /* Unused if macro */

                        const char delim = TOKEN_EQUALS;
                        const char *data = strtok(line, &delim);

                        if(!safe_strcpy(identry->name, data, MAX_ID)) {     /* Copy macro name */

                            printf("[%s, %llu] Macro name too long!\n", _file, lineidx);
                            THROW(EX_BYTECODE);
                            return 0;
                        }
                        data = strtok(NULLPTR, &delim);             /* Get macro value */
                        if(!safe_strcpy(identry->value, data, MAX_ID_VAL)) {    /* Copy macro name */

                            printf("[%s, %llu] Macro value too long!\n", _file, lineidx);
                            THROW(EX_BYTECODE);
                            return 0;
                        }

                        if(id_table_find_duplicates(idtable, identry) != 1) {

                            printf("[%s, %llu] Macro already defined: %s!\n", _file, lineidx, identry->name);
                            THROW(EX_BYTECODE);
                            return 0;
                        }
                    } break;

                    case PPI_NUM:

                         printf("[%s, %llu] Invalid preprocessor directive: %s!\n", _file, lineidx, line);

                    break;
                }

            } break;

            case TOKEN_PIN: {

                ++line;

                /* Validate pin name */
                if(!*line || *line == TOKEN_COLON || isdigit(*line)) {
                    printf("[%s, %llu] Invalid PIN name: %s! PINs names cannot start with a digit!\n", _file, lineidx, line);
                    THROW(EX_BYTECODE);
                    return 0;
                }

                if(!strchr(line, TOKEN_COLON)) {

                    printf("[%s, %llu] Missing %c after PIN!\n", _file, lineidx, TOKEN_COLON);
                    THROW(EX_BYTECODE);
                    return 0;
                }

                auto struct id *identry;
                id_table_push(idtable, &identry);
                if(EXCEPT()) {

                    printf("Failed to push id table!\n");
                    return 0;
                }

                identry->type = IDT_PIN;
                identry->address = pr->num;   /*  If this at at the beginning of the code set offset to zero, else to count -1 to get corrent index */

                /* Copy label name */

                if(!safe_strcpy(identry->name, line, MAX_ID)) {     /* Copy macro name */

                    printf("[%s, %llu] Macro name too long!\n", _file, lineidx);
                    THROW(EX_BYTECODE);
                    return 0;
                }

                /* Delete colon: */
                char *const colon = strchr(identry->name, TOKEN_COLON);
                if(!colon) {

                    printf("[%s, %llu] Missing %c after PIN!\n", _file, lineidx, TOKEN_COLON);
                    THROW(EX_BYTECODE);
                    return 0;
                }
                *colon = '\0';

                if(id_table_find_duplicates(idtable, identry) != 1) {

                    printf("[%s, %llu] PIN already defined: %s!\n", _file, lineidx, identry->name);
                    THROW(EX_BYTECODE);
                    return 0;
                }

            } break;

            default: {

                auto const struct op_meta_t *op;
                auto const enum op_t instr = determine_instruction(&line, &op);

                if(!op || csec != SEC_EXEC) {

                    printf("[%s, %llu] Invalid instruction '%s'!\n", _file, lineidx, line);
                    THROW(EX_BYTECODE);
                    return 0;
                }

                /* If instruction has parameters */
                if(op->para_expl_num > 0) {

                    prog_put_op(pr, instr); /* Add instruction to bytecode */

                    auto enum int_flags intfl;
                    auto enum oppt_t pp;
                    auto const char delim = TOKEN_DELIMITER;

                    auto signed char i;
                    for(i = 0; i < op->para_expl_num; ++i) {

                        if(op->para_expl_num > 1) {

                            line = strtok(!i?line:NULLPTR, &delim);
                        }

                        if(!line || *line == '\0') {

                            printf("[%s, %llu] %s Missing parameter!\n", _file, lineidx, line);
                            THROW(EX_BYTECODE);
                            return 0;
                        }

                        pp = determine_param_type(&line, &intfl);

                        if(pp == OPPT_NONE || (op->para_prim != OPPT_HPARAM && pp != op->para_prim)) {

                            printf("[%s, %llu] %s Invalid param type! Change sigil or value!\n", _file, lineidx, line);
                            THROW(EX_BYTECODE);
                            return 0;
                        }

                        auto int ok = FALSE;
                        auto union signal_t val = {0};

                        switch(pp) {

                            case OPPT_INTID: {

                                ok = str_2_int(line, &val.i, intfl);

                                /* Check if legacy intrinsic call ID is out of bounds */
                                if(val.i >= 0 && val.i > IPCID_NUM) {

                                    printf("Legacy intrinsic call id it out of range! Allowed values are from 0x0 to 0x%x!\n", IPCID_NUM - 1);
                                    ok = FALSE;
                                }

                                auto unsigned long long cintrin_num;
                                pre_bind_cintrin(NULLPTR, &cintrin_num);

                                if(val.i < 0 && val.i <-((int)cintrin_num) - 1) {

                                    printf("Custom intrinsic call id it out of range! Allowed values are from -0x1 to -0x%llu!\n", cintrin_num);
                                    ok = FALSE;
                                }

                            }   break;

                            default:
                            case OPPT_IPARAM:

                                ok = str_2_int(line, &val.i, intfl);

                                break;

                            case OPPT_FPARAM:

                                ok = str_2_float(line, &val.f);

                                break;

                            case OPPT_PPID: {

                                /* Search for label in type id table */
                                register struct id *v;
                                register unsigned long long i;

                                for(i = 0; i < idtable->num; ++i) {

                                    v = idtable->dat + i;
                                    if(!strncmp(line, v->name, strlen(v->name))) {

                                        ok = TRUE;
                                        val.i = v->address;
                                    }
                                }

                                if(!ok) {

                                    printf("[%s, %llu] Undefined PIN: %s!\n", _file, lineidx, line);
                                    THROW(EX_BYTECODE);
                                    return 0;
                                }

                            } break;

                        }

                        if(ok && line) {

                            prog_put_sig(pr, val);  /* Add parameter to bytecode */
                        }
                        else {

                            printf("[%s, %llu] Invalid parameter: %s!\n", _file, lineidx, line);
                            THROW(EX_BYTECODE);
                            return 0;
                        }
                    }

                } else {

                    if(*line != '\0') {

                        printf("[%s, %llu] %s << Redudant parameter!\n", _file, lineidx, line);
                        THROW(EX_BYTECODE);
                        return 0;
                    }

                    prog_put_op(pr, instr); /* Add instruction to bytecode */
                }
            } break;
        }
    }

    /*
    for(register struct id *i = idtable->dat, *const end = idtable->dat + idtable->num; i < end; ++i) {

        printf("IP: %zu, ID: %s, Type: %i, Val: %s!\n", i->address, i->name, i->type, i->value);
    }
    */

    file_close(&filestream); /* Close file stream */
    if(EXCEPT()) {

        printf("Failed to close file: %s!\n", _file);
        return 0;
    }

    id_table_destroy(&idtable); /* Release id table memory */

    if(!(_flags & INF_BYPASS_AUTO_INTERRUPT) && pr->dat[pr->num - 2].op != OP_INT) {

        prog_put_op(pr, OP_INT);
        prog_put_i(pr, 0);
    }

    if(!(_flags & INF_BYPASS_PEFECT_FIT_RESIZE)) {

        prog_resize_perfect(pr);
    }

#ifdef DEBUG_VERBOSE
    printf("Parsed %s with %llu lines in %f!\n", _file, lineidx ,(double)(clock() - tik) / CLOCKS_PER_SEC);
#endif

    CLEAR_EXSTATUS();
    return lineidx;
}
